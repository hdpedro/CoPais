function setPath(obj, dotted, value) {
  const parts = dotted.split(".");
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    const p = parts[i];
    if (typeof cur[p] !== "object" || cur[p] === null) {
      cur[p] = {};
    }
    cur = cur[p];
  }
  cur[parts[parts.length - 1]] = value;
}

function hasPath(obj, dotted) {
  const parts = dotted.split(".");
  let cur = obj;
  for (const p of parts) {
    if (typeof cur !== "object" || cur === null || !(p in cur)) return false;
    cur = cur[p];
  }
  return true;
}

(async () => {
  for (const locale of LOCALES) {
    const path = resolve(ROOT, `src/i18n/locales/${locale}.json`);
    const raw = await readFile(path, "utf8");
    const data = JSON.parse(raw);
    const updates = UPDATES[locale] ?? {};
    let added = 0;
    for (const [k, v] of Object.entries(updates)) {
      if (hasPath(data, k)) continue;
      setPath(data, k, v);
      added++;
    }
    await writeFile(path, JSON.stringify(data, null, 2) + "\n", "utf8");
    console.log(`${locale}: +${added} keys`);
  }
})();
